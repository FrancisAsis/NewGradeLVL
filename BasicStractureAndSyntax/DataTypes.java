package BasicStractureAndSyntax;

public class DataTypes {

    public static void main(String[] args) {
        System.out.println("=====Data Types in Java=====");
        byte myByte = 100;
        System.out.println("Byte value: " + myByte);

        short myShort = 1000;
        System.out.println("Short value: " + myShort);

        int myInt = 10000;
        System.out.println("Int value: " + myInt);

        long myLong = 100000L;
        System.out.println("Long value: " + myLong);

        float myFloat = 10.5f;
        System.out.println("Float value: " + myFloat);

        double myDouble = 10.5d;
        System.out.println("Double value: " + myDouble);

        boolean myBoolean = true;
        System.out.println("Boolean value: " + myBoolean);

        float ScienceFloat = 1.23e2f; // 1.23 * 10^2
        System.out.println("Scientific notation float value: " + ScienceFloat);

        double ScienceDouble = 1.23e3d; // 1.23 * 10^3
        System.out.println("Scientific notation double value: " + ScienceDouble);

        char myChar = 'A';
        System.out.println("Char value: " + myChar);

        char myVar1 = 36, myVar2 = 40, myVar3 = 41;
        System.out.println(myVar1); // Output: A
        System.out.println(myVar2); // Output: B
        System.out.println(myVar3); // Output: C

        




    }
    
}
